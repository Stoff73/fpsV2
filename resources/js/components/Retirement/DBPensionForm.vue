<template>
  <div class="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50 p-4" @click.self="$emit('close')">
    <div class="bg-white rounded-lg shadow-xl max-w-2xl w-full max-h-[90vh] overflow-y-auto">
      <!-- Header -->
      <div class="sticky top-0 bg-white border-b border-gray-200 px-6 py-4 flex items-center justify-between">
        <h3 class="text-xl font-semibold text-gray-900">
          {{ isEdit ? 'Edit' : 'Add' }} DB Pension
        </h3>
        <button @click="$emit('close')" class="text-gray-400 hover:text-gray-600 transition-colors">
          <svg class="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12"></path>
          </svg>
        </button>
      </div>

      <!-- Important Warning -->
      <div class="mx-6 mt-6 bg-amber-50 border-2 border-amber-400 rounded-lg p-4 flex items-start">
        <svg class="w-6 h-6 text-amber-600 mr-3 flex-shrink-0 mt-0.5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 9v2m0 4h.01m-6.938 4h13.856c1.54 0 2.502-1.667 1.732-3L13.732 4c-.77-1.333-2.694-1.333-3.464 0L3.34 16c-.77 1.333.192 3 1.732 3z"></path>
        </svg>
        <div>
          <p class="text-sm font-bold text-amber-900">Important Notice About DB Pensions</p>
          <p class="text-sm text-amber-800 mt-2">
            DB pension information is captured for <strong>income projection only</strong>.
            This system does <strong>not provide DB to DC transfer advice</strong>.
            DB pension transfers are complex and may not be suitable.
            You should seek specialist financial advice before considering any transfer.
          </p>
        </div>
      </div>

      <!-- Form -->
      <form @submit.prevent="handleSubmit" class="p-6">
        <div class="space-y-6">
          <!-- Employer Name -->
          <div>
            <label for="employer_name" class="block text-sm font-medium text-gray-700 mb-2">
              Employer / Scheme Name <span class="text-red-500">*</span>
            </label>
            <input
              id="employer_name"
              v-model="formData.employer_name"
              type="text"
              required
              class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
              placeholder="e.g., NHS Pension Scheme"
            />
          </div>

          <!-- Scheme Status and Type -->
          <div class="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div>
              <label for="scheme_status" class="block text-sm font-medium text-gray-700 mb-2">
                Scheme Status <span class="text-red-500">*</span>
              </label>
              <select
                id="scheme_status"
                v-model="formData.scheme_status"
                required
                class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
              >
                <option value="">Select status</option>
                <option value="Active">Active</option>
                <option value="Deferred">Deferred</option>
                <option value="In Payment">In Payment</option>
              </select>
            </div>
            <div>
              <label for="scheme_type" class="block text-sm font-medium text-gray-700 mb-2">
                Scheme Type
              </label>
              <select
                id="scheme_type"
                v-model="formData.scheme_type"
                class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
              >
                <option value="final_salary">Final Salary</option>
                <option value="career_average">Career Average (CARE)</option>
              </select>
            </div>
          </div>

          <!-- Annual Income -->
          <div>
            <label for="annual_income" class="block text-sm font-medium text-gray-700 mb-2">
              Annual Income at Retirement (£) <span class="text-red-500">*</span>
            </label>
            <input
              id="annual_income"
              v-model.number="formData.annual_income"
              type="number"
              step="0.01"
              min="0"
              required
              class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
              placeholder="e.g., 15000.00"
            />
            <p class="text-xs text-gray-500 mt-1">This should be the projected annual pension at your normal retirement age</p>
          </div>

          <!-- Service Years and Final/Pensionable Salary -->
          <div class="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div>
              <label for="service_years" class="block text-sm font-medium text-gray-700 mb-2">
                Service Years <span class="text-red-500">*</span>
              </label>
              <input
                id="service_years"
                v-model.number="formData.service_years"
                type="number"
                step="0.1"
                min="0"
                required
                class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                placeholder="e.g., 20.5"
              />
            </div>
            <div>
              <label for="final_salary" class="block text-sm font-medium text-gray-700 mb-2">
                Pensionable Salary (£)
              </label>
              <input
                id="final_salary"
                v-model.number="formData.final_salary"
                type="number"
                step="0.01"
                min="0"
                class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                placeholder="e.g., 50000.00"
              />
            </div>
          </div>

          <!-- Accrual Rate and Normal Retirement Age -->
          <div class="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div>
              <label for="accrual_rate" class="block text-sm font-medium text-gray-700 mb-2">
                Accrual Rate (1/X)
              </label>
              <input
                id="accrual_rate"
                v-model.number="formData.accrual_rate"
                type="number"
                min="0"
                class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                placeholder="e.g., 60 (for 1/60th)"
              />
              <p class="text-xs text-gray-500 mt-1">Common: 60 (public sector), 80 (older schemes)</p>
            </div>
            <div>
              <label for="normal_retirement_age" class="block text-sm font-medium text-gray-700 mb-2">
                Normal Retirement Age
              </label>
              <input
                id="normal_retirement_age"
                v-model.number="formData.normal_retirement_age"
                type="number"
                min="50"
                max="75"
                class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                placeholder="e.g., 65"
              />
            </div>
          </div>

          <!-- Revaluation Rate -->
          <div>
            <label for="revaluation_rate" class="block text-sm font-medium text-gray-700 mb-2">
              Revaluation Rate (% p.a.)
            </label>
            <input
              id="revaluation_rate"
              v-model.number="formData.revaluation_rate"
              type="number"
              step="0.01"
              min="0"
              max="10"
              class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
              placeholder="e.g., 2.50"
            />
            <p class="text-xs text-gray-500 mt-1">For CARE schemes - typical: CPI, CPI+1.5%, or fixed %</p>
          </div>

          <!-- PCLS Available -->
          <div>
            <label for="pcls_available" class="block text-sm font-medium text-gray-700 mb-2">
              Pension Commencement Lump Sum (PCLS) Available (£)
            </label>
            <input
              id="pcls_available"
              v-model.number="formData.pcls_available"
              type="number"
              step="0.01"
              min="0"
              class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
              placeholder="e.g., 50000.00"
            />
            <p class="text-xs text-gray-500 mt-1">Tax-free lump sum available at retirement (if applicable)</p>
          </div>

          <!-- Notes -->
          <div>
            <label for="notes" class="block text-sm font-medium text-gray-700 mb-2">
              Notes
            </label>
            <textarea
              id="notes"
              v-model="formData.notes"
              rows="3"
              class="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
              placeholder="Any additional notes about this pension..."
            ></textarea>
          </div>
        </div>

        <!-- Actions -->
        <div class="flex items-center justify-end space-x-3 mt-8 pt-6 border-t border-gray-200">
          <button
            type="button"
            @click="$emit('close')"
            class="px-4 py-2 text-gray-700 bg-white border border-gray-300 rounded-lg hover:bg-gray-50 transition-colors duration-200"
          >
            Cancel
          </button>
          <button
            type="submit"
            class="px-6 py-2 bg-indigo-600 text-white rounded-lg hover:bg-indigo-700 transition-colors duration-200"
          >
            {{ isEdit ? 'Update' : 'Add' }} Pension
          </button>
        </div>
      </form>
    </div>
  </div>
</template>

<script>
export default {
  name: 'DBPensionForm',

  props: {
    pension: {
      type: Object,
      default: null,
    },
    isEdit: {
      type: Boolean,
      default: false,
    },
  },

  data() {
    return {
      formData: {
        employer_name: '',
        scheme_status: '',
        scheme_type: 'final_salary',
        annual_income: null,
        service_years: null,
        final_salary: null,
        accrual_rate: null,
        normal_retirement_age: 65,
        revaluation_rate: null,
        pcls_available: null,
        notes: '',
      },
    };
  },

  mounted() {
    if (this.isEdit && this.pension) {
      this.formData = { ...this.pension };
    }
  },

  methods: {
    handleSubmit() {
      // Basic validation
      if (!this.formData.employer_name) {
        alert('Please enter an employer/scheme name');
        return;
      }

      if (!this.formData.scheme_status) {
        alert('Please select a scheme status');
        return;
      }

      if (!this.formData.annual_income || this.formData.annual_income < 0) {
        alert('Please enter a valid annual income');
        return;
      }

      if (!this.formData.service_years || this.formData.service_years < 0) {
        alert('Please enter valid service years');
        return;
      }

      this.$emit('save', this.formData);
    },
  },
};
</script>

